const berries = require('./index');
test('positive', ()=> {expect(berries(12, 13, 5)).toBe(20);});
test('border value - first left', ()=> {expect(berries(0, 4, 1)).toBe(3);});
test('border values - first right', ()=> {expect(berries(1000, 5, 100)).toBe(905);});
test('border values - second left', ()=> {expect(berries(40, 0, 35)).toBe(5);});
test('border values - second right', ()=> {expect(berries(70, 1000, 350)).toBe(720);});
test('border values - third left', ()=> {expect(berries(34, 67, 0)).toBe(101);});
test('border values - third right', ()=> {expect(berries(1000, 1000, 2000)).toBe(0);});
test('negative - two arguments', ()=> {expect(berries(12, 44)).toBe(-1);});
test('negative - one arguments', ()=> {expect(berries(678)).toBe(-1);});
test('negative - zero arguments', ()=> {expect(berries()).toBe(-1);});
test('negative - so much arguments', ()=> {expect(berries(234, 456, 34, 456)).toBe(-2);});
test('negative - first not an integer', ()=> {expect(berries(14.4, 45, 50)).toBe(-3);});
test('negative - second not an integer', ()=> {expect(berries(564, 345.23, 400)).toBe(-3);});
test('negative - third not an integer', ()=> {expect(berries(73, 409, 76.9)).toBe(-3);});
test('negative - first not a number', ()=> {expect(berries('c', 456, 45)).toBe(-3);});
test('negative - second not a number', ()=> {expect(berries(125, true, 45)).toBe(-3);});
test('negative - third not a number', ()=> {expect(berries(123, 765, '34m5')).toBe(-3);});
test('negative - going beyond the left border of the first number', ()=> {expect(berries(-23, 234, 45)).toBe(-4);});
test('negative - going beyond the right border of the first number', ()=> {expect(berries(1456, 344, 500)).toBe(-4);});
test('negative - going beyond the left border of the second number', ()=> {expect(berries(234, -56, 126)).toBe(-4);});
test('negative - going beyond the right border of the second number', ()=> {expect(berries(563, 4506, 1980)).toBe(-4);});
test('negative - going beyond the left border of the third number', ()=> {expect(berries(456, 565, -4)).toBe(-4);});
test('negative - going beyond the right border of the third number', ()=> {expect(berries(654, 65, 3059)).toBe(-4);});
test('negative - the third argument bigger than X+Y', ()=> {expect(berries(673, 892, 1999)).toBe(-5);});